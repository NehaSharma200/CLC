def add(a, b):
    c = a + b
    return c


def sub(p, q):
    r = p - q
    return r


def mul(d, e):
    f = d * e
    return f


def div(t, s):
    u = s / t
    return u


def exp(g, h):
    i = g ** h
    return i


print(add(12, 84))
print(sub(85, 75))
print(mul(74, 87))
print(div(45, 87))
print(exp(5, 4))
